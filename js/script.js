$("#bas").on('change', function() {
    if ($(this).is(':checked')) {
        $("#bas_sa").prop("disabled", false);
        $("#bas_pa").prop("disabled", false);
    } else {
        $("#bas_sa").val('');
        $("#bas_sa").prop("disabled", true);
        $("#bas_pa").val('');
        $("#bas_pa").prop("disabled", true);
    }
});

